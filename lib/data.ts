/**
 * Единственный источник правды по сетке — **финальный файл программы от организаторов** (последняя присланная версия).
 * Пример имени/пути для сверки: `Программа ФИНАЛ 13.05 (1) (1).xlsx` · лист «Основная программа» (строки 1–71).
 * **Живой сайт** отдаёт уже заложенные здесь `SPEAKERS` / `SESSIONS` (на Vercel нет доступа к вашему диску) — после новой версии XLSX обновляйте этот файл и деплой.
 * Пустые ячейки и «??????» в финальном XLSX **не додумываем**: в UI остаются как в источнике (организационные слоты / пояснение в описании).
 *
 * Примечание по строке 18: в правом столбце перечислена Авруцкая — в детальных слотах дня 1 (стр. 19–26) её нет;
 *   сетка главного зала сверена по колонкам A–D.
 * Лист «ПР для СПИК» — вспомогательный тайминг для спикеров.
 *
 * SPEAKERS: поле role и ФИО — из колонки «Спикеры / Участники» дампа; bio и city не заполняются,
 * география и организация — только внутри role, если есть в ячейке.
 */
const D1 = '2026-05-16'
const D2 = '2026-05-17'

function st(day: 1 | 2, start: string, end: string): Pick<Session, 'starts_at' | 'ends_at'> {
  const d = day === 1 ? D1 : D2
  const sh = start.length === 5 ? `${start}:00` : start
  const eh = end.length === 5 ? `${end}:00` : end
  return { starts_at: `${d}T${sh}`, ends_at: `${d}T${eh}` }
}

